package arithmetic_expressions;

public class ArithmeticExpressions {
    public static void main(String[] args) {

        int friends = 10;

        // add
        friends = friends + 1;
        System.out.println(friends);

        // sub
        friends = friends - 3;
        System.out.println(friends);

        // multiply
        friends = friends * 2;
        System.out.println(friends);

        // division
        friends = friends / 2;
        System.out.println(friends);

        // modulus
        friends = friends % 3;
        System.out.println(friends);

    }
}
